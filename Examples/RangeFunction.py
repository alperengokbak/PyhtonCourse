# Range function provides to find numbers 1 to 100.
# Run the code just between blue side.
#%% Range Example
for x in range(50):
    print(x)
#%% Range Example 2
for x in range(1 , 10):     # Starting from 1 to 9. Not include number 10. Usually dont contain the edges in python.
    print(x)

#%% Range Example 3
for x in range(1 , 10 , 2):     # Again starting from 1 and going to 9 but in twos. (Default setting is in ones.)
    print(x)